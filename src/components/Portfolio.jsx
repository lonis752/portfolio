function Portfolio() {
  const ref = useRef();

  const { scrollYProgress } = useScroll({
    target: ref,
    offset: ["end end", "start start"],
  });

  const scaleX = useSpring(scrollYProgress, {
    stiffness: 100,
    damping: 30,
  });

  return (
    <div ref={ref} className="relative">
      <div className="sticky top-0 left-0 pt-20 text-center text-orange-300 font-bold text-5xl">
        <h1>Featured Projects</h1>
        <motion.div
          style={{ scaleX }}
          className="h-[5px] bg-white mt-5 rounded-2xl"
        ></motion.div>
      </div>
      {items.map((item) => (
        <Single item={item} key={item.id} />
      ))}
    </div>
  );
}

export default Portfolio;
